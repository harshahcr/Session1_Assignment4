#Define values for variables 
d=12
print('The diameter of the sphere is d =', d ,'cm')
r=int(d/2)
print('The radius of the sphere derived r =',r ,'cm')
#import the math lib for pi values
import math
pi1=math.pi
v=4/3*pi1*r**3
print('The volume of the sphere with diameter 12cms is ',v)

